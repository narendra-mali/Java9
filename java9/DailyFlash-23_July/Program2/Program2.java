

class Angle {
	
	public static void main(String args[]) {

		int angle = 20;
		
		if(angle < 90) {
			
			System.out.println(angle + " = Acute Angle");
		}
		
		else if(angle > 90) {
				
			System.out.println(angle + " = Obtuse Angle");
		}
		
		else {
			
			System.out.println(angle + " = Right Angle");
		}
	}
}
