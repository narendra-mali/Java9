

class Program1 {

	public static void main(String[] args) {

        char ch = 'v';

        if((ch >= 'A' && ch <= 'Z') || (ch >= 'a' && ch <= 'z'))
           
	       	System.out.println(ch + " is an alphabet");
        
	else
        
	    	System.out.println(ch + " is not an alphabet");
    }
}
